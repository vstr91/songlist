package br.com.vostre.songlist.model.pojo;

public class Playlist {

    String nome;

    Integer musicas;

    public String getNome() {
        return nome;
    }

    public void setNome(String nome) {
        this.nome = nome;
    }

    public Integer getMusicas() {
        return musicas;
    }

    public void setMusicas(Integer musicas) {
        this.musicas = musicas;
    }
}
